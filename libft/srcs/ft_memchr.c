#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned const char	*tstr;
	unsigned char		tc;
	size_t				i;

	tstr = (unsigned char*)str;
	tc = (unsigned char)c;
	i = -1;
	while (++i < n)
		if (tstr[i] == tc)
			return ((void *)&tstr[i]);
	return (NULL);
}
